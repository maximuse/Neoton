    private void loadData() {
        this.emf = Persistence.createEntityManagerFactory("NeotonPU");
        this.ajc = new AlbumokJpaController(emf);
        this.albumok = new ArrayList<>();
        this.albumok = ajc.findAlbumokEntities();
        
        this.jComboBox1.removeAllItems();
        this.jComboBox1.addItem("- Kérem válasszon albumot! -");
        
        for (Albumok album : this.albumok) {
            this.jComboBox1.addItem(album.getNev());
        }
    }
    
