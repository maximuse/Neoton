    private void jComboBox1ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBox1ItemStateChanged
        int index = this.jComboBox1.getSelectedIndex();
        
        if (index > 0) {
            try {
                this.boritoLabel.setIcon(new javax.swing.ImageIcon(getClass().getResource("/view/img/" + this.albumok.get(index - 1).getBorito())));
            }
            catch(Exception e) {
                this.boritoLabel.setIcon(new javax.swing.ImageIcon(getClass().getResource("/view/img/bakelit.png")));
            }
            
            this.cimLabel.setText(this.albumok.get(index - 1).getNev());
            this.evLabel.setText(this.albumok.get(index - 1).getEv() + "");
        }
        else {
            this.boritoLabel.setIcon(new javax.swing.ImageIcon(getClass().getResource("/view/img/bakelit.png")));
            this.cimLabel.setText("");
            this.evLabel.setText("");
        }
    }//GEN-LAST:event_jComboBox1ItemStateChanged

