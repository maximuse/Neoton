    private void mentesButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mentesButtonActionPerformed
        String nev = this.nevField.getText();
        int ev = Integer.parseInt(this.evSpinner.getValue() + "");
        String kep = this.kepField.getText();
        
        if (nev.equals("")) {
            JOptionPane.showMessageDialog(null, "Nem adta meg a nevet!");
        }
        else if (kep.equals("")) {
            JOptionPane.showMessageDialog(null, "Nem adta meg a kép nevét!");
        }
        else {
            Albumok ujAlbum = new Albumok();
            ujAlbum.setNev(nev);
            ujAlbum.setEv(ev);
            ujAlbum.setBorito(kep);
            this.ajc.create(ujAlbum);
            
            JOptionPane.showMessageDialog(null, "Sikeres mentés!");
            
            loadData();
        }
    }//GEN-LAST:event_mentesButtonActionPerformed

